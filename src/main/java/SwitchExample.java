public class SwitchExample {

    public static void main(String[] args) {
        int i = 3;

        switch (i){
            case 3:
                System.out.println("3");
                break;
            case 4:
                System.out.println("4");
                break;
            default:
                System.out.println("anderer Wert");
        }

    }

}
